namespace Mathy
{
    double Calculate::Add(double a, double b)
    {
        return a + b;
    }

    double Calculate::Subtract(double a, double b)
    {
        return a - b;
    }

    double Calculate::Multiply(double a, double b)
    {
        return a * b;
    }

    double Calculate::Divide(double a, double b)
    {
        return a / b;
    }
}